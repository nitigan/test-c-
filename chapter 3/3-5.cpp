#include <iostream>
using namespace std;
int main()
{
    cout << " Display Integer Constants "<<endl;
    cout << " Octal Constant 023 is "<< 023 <<endl;
    cout << " Decimal Constant 23 is "<< 23 <<endl;
    cout << " Hexa Constant 0x23 is "<< 0x23 <<endl;
    cout << 230.E+3 << endl;
    cout << 230E3 << endl;
    cout << 230000.0 << endl;
    cout << 2.3e5 << endl;
    cout << 0.23E6 << endl;
    cout << .23e+6 << endl;
    system("pause");
    return (0);
}